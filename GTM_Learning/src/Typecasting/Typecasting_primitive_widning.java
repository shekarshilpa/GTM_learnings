package Typecasting;

public class Typecasting_primitive_widning {

	public static void main(String[] args) 
	{
		// convert int to double = widning
		
		double a1 = 100; //implicit widning
		System.out.println(a1);
		
		double a2 = (double)100; //explicit widning
		System.out.println(a2);
		
	
		

	}

}
